# 测试对象的布尔值
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(None))
print(bool(''))
print(bool(""))
print(bool([])) #空列表
print(bool(list()))
print(bool(())) # 空元祖
print(bool(tuple()))
print(bool({}))# 空字典
print(bool(dict()))#空字典
print(bool(set()))#空集合
# 其他对象的布尔值均为True
